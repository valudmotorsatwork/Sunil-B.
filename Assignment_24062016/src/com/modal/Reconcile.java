package com.modal;

public class Reconcile 
{
	private String conBranch;
	private String conDate;
	private String conBranchCode;
	private String conTotRecon;
	private String conReconDiff;
	private String comCompany;
	public String getConBranch() {
		return conBranch;
	}
	public void setConBranch(String conBranch) {
		this.conBranch = conBranch;
	}
	public String getConDate() {
		return conDate;
	}
	public void setConDate(String conDate) {
		this.conDate = conDate;
	}
	public String getConBranchCode() {
		return conBranchCode;
	}
	public void setConBranchCode(String conBranchCode) {
		this.conBranchCode = conBranchCode;
	}
	public String getConTotRecon() {
		return conTotRecon;
	}
	public void setConTotRecon(String conTotRecon) {
		this.conTotRecon = conTotRecon;
	}
	public String getConReconDiff() {
		return conReconDiff;
	}
	public void setConReconDiff(String conReconDiff) {
		this.conReconDiff = conReconDiff;
	}
	public String getComCompany() {
		return comCompany;
	}
	public void setComCompany(String comCompany) {
		this.comCompany = comCompany;
	}

}
